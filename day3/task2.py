# For loop using tuple
my_tuple = (1, 2, 3, 4, 5)
for item in my_tuple:
    print(f"Tuple item: {item}")

# For loop using dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}
for key, value in my_dict.items():
    print(f"Dictionary key: {key}, value: {value}")

# For loop using set
my_set = {10, 20, 30, 40}
for item in my_set:
    print(f"Set item: {item}")
